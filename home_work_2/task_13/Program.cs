Console.Clear();
Console.WriteLine("Введите чило");
int n = Convert.ToInt32(Console.ReadLine());
int x = (n / 10) % 10;
int y = (n / 100) % 10;
int z = n % 10;
int r = (n / 1000) % 10;
if(n < 100) Console.WriteLine("Третьего числа нет");
if(n > 99 && n < 1000) Console.WriteLine(z);
if(n > 999 && n < 10000) Console.WriteLine(x);
if(n > 9999 && n < 100000) Console.WriteLine(y);
if(n > 99999 && n < 1000000) Console.WriteLine(r);
if(n > 999999) Console.WriteLine("Не надо вводить такие огромные числа");
